#include "libft.h"

int	ft_nb_digits(int n)
{
	int	result;

	if (n == -2147483648)
		return (10);
	if (n < 0)
		n *= -1;
	result = 1;
	while (n >= 10)
	{
		n /= 10;
		result++;
	}
	return (result);
}
